#include "command_queue.h"

/*============================ หฝ๏ฟฝะฑ๏ฟฝ๏ฟฝ๏ฟฝ ============================*/

/**
 * @brief ๏ฟฝ๏ฟฝ๏ฟฝฮถ๏ฟฝ๏ฟฝะป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 *
 * queue_buffer[] ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝลตศด๏ฟฝึด๏ฟฝะต๏ฟฝ๏ฟฝหถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๎กฃ
 *
 * Parser:
 *      queue_push()
 *
 * Planner:
 *      queue_pop()
 */
static MotionCmd queue_buffer[COMMAND_QUEUE_SIZE];

/**
 * @brief ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝรฃ๏ฟฝ
 *
 * ึธ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ Planner ศก๏ฟฝ฿ต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๎กฃ
 */
static uint16_t tail = 0;

/**
 * @brief ๏ฟฝ๏ฟฝฮฒ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝรฃ๏ฟฝ
 *
 * ึธ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝืผ๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝรก๏ฟฝ
 */
static uint16_t head = 0;

/**
 * @brief ๏ฟฝ๏ฟฝวฐ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 */
static uint16_t count = 0;


/*============================ ๏ฟฝำฟ๏ฟฝสต๏ฟฝ๏ฟฝ ============================*/

/**
 * @brief ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 */
void queue_init(void)
{
    head = 0;
    tail = 0;
    count = 0;
}


/**
 * @brief ๏ฟฝ๏ฟฝีถ๏ฟฝ๏ฟฝ๏ฟฝ
 */
void queue_clear(void)
{
    head = 0;
    tail = 0;
    count = 0;
}


/**
 * @brief ๏ฟฝะถฯถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝฮช๏ฟฝ๏ฟฝ
 */
bool queue_is_empty(void)
{
    return (count == 0);
}


/**
 * @brief ๏ฟฝะถฯถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 */
bool queue_is_full(void)
{
    return (count >= COMMAND_QUEUE_SIZE);
}


/**
 * @brief ๏ฟฝ๏ฟฝศก๏ฟฝ๏ฟฝวฐ๏ฟฝ๏ฟฝ๏ฟฝะณ๏ฟฝ๏ฟฝ๏ฟฝ
 */
uint16_t queue_size(void)
{
    return count;
}


/**
 * @brief ๏ฟฝ๏ฟฝ๏ฟฝ
 *
 * @param cmd
 *      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะต๏ฟฝ๏ฟฝหถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 *
 * @retval true
 *      ๏ฟฝ๏ฟฝำณษน๏ฟฝ
 *
 * @retval false
 *      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 */
bool queue_push(const MotionCmd *cmd)
{
    /*------------------------
      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    ------------------------*/

    if(cmd == NULL)
    {
        return false;
    }

    /*------------------------
      ๏ฟฝะถฯถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    ------------------------*/

    if(queue_is_full())
    {
        extern volatile uint32_t dbg_queue_full;
        dbg_queue_full++;
        return false;
    }

    extern volatile uint32_t dbg_queue_push;
    dbg_queue_push++;

    /*------------------------
      ะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    ------------------------*/

    queue_buffer[head] = *cmd;

    /*------------------------
      head๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦถ๏ฟฝาป๏ฟฝ๏ฟฝ
      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฉฮฒ๏ฟฝ๏ฟฝ๏ฟฝิถ๏ฟฝ๏ฟฝุต๏ฟฝ0
    ------------------------*/

    head++;

    if(head >= COMMAND_QUEUE_SIZE)
    {
        head = 0;
    }

    /*------------------------
      ๏ฟฝ๏ฟฝวฐ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ+1
    ------------------------*/

    count++;

    return true;
}


/**
 * @brief ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 *
 * @param cmd
 *      ๏ฟฝ๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝหถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
 *
 * @retval true
 *      ๏ฟฝ๏ฟฝ๏ฟฝำณษน๏ฟฝ
 *
 * @retval false
 *      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮช๏ฟฝ๏ฟฝ
 */
bool queue_pop(MotionCmd *cmd)
{
    /*------------------------
      ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    ------------------------*/

    if(cmd == NULL)
    {
        return false;
    }

    /*------------------------
      ๏ฟฝะถฯถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝฮช๏ฟฝ๏ฟฝ
    ------------------------*/

    if(queue_is_empty())
    {
        return false;
    }

    /*------------------------
      ศก๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    ------------------------*/

    *cmd = queue_buffer[tail];

    /*------------------------
      tail๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦถ๏ฟฝ
    ------------------------*/

    tail++;

    if(tail >= COMMAND_QUEUE_SIZE)
    {
        tail = 0;
    }

    /*------------------------
      ๏ฟฝ๏ฟฝวฐ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ-1
    ------------------------*/

    count--;

    return true;
}

